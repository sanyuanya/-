<?php

/*

 * 作用：pow函数的作用是将一个数进行N次方计算返回
 * pow(x,y)
 * 参数	描述
    X	要做处理的数字
    Y	指定n次方中的n数值

    说明：返回X的Y次方幂，如果参数正确，函数会返回一个数值，如果无法计算，会返回NAN。
 *  */
$i = 5;
$i = pow($i,3);
$j = 6.5;
$j = pow($j,4);
$k = -5;
$k = pow($k,5);
$h = -0.5;
$h = pow($h,0.6);
echo $i."*****".$j."*****".$k."*****".$h;
?>
